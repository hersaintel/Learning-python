"""
Arithmetic operators are used with numeric values to perform common mathematical operations:
+ 	Addition 	    x + y 	
- 	Subtraction 	x - y 	
* 	Multiplication 	x * y 	
/ 	Division 	    x / y 	
% 	Modulus 	    x % y 	
** 	Exponentiation 	x ** y 	
// 	Floor division 	x // y
"""
#Example
x = 15
y = 4

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)
print(x ** y)
print(x // y)

"""
Division in Python

Python has two division operators:

    / - Division (returns a float)
    // - Floor division (returns an integer)
"""
#Example
#Division always returns a float:
x = 12
y = 5
print(x / y)

#Floor division returns an integer
x = 12
y = 5
print(x // y)